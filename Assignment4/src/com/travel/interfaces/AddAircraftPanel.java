    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        boolean validation = true;
        String modelNum = txtModelNum.getText();
        if("".equals(modelNum)){
                JOptionPane.showMessageDialog(this, "Registration cannot be empty!");
                modelNumLabel.setForeground(Color.red);
                validation = false;
                return;
            }
        else if(aircraftList.sameModelNum(modelNum) == true){
            JOptionPane.showMessageDialog(this, "Registration already existed!");
                modelNumLabel.setForeground(Color.red);
                validation = false;
                return;
        }
             else{
            modelNumLabel.setForeground(Color.black);
            validation = true;
        }
        String model = txtModel.getText();
        if("".equals(model)){
                JOptionPane.showMessageDialog(this, "Model cannot be empty!");
                modelLabel.setForeground(Color.red);
                validation = false;
                return;
            }
        
             else{
            modelLabel.setForeground(Color.black);
            validation = true;
        }
        
        
        if(validation == true) {
            Aircraft aircraft = aircraftList.addAircraft();
            aircraft.setModelNum(modelNum);
            aircraft.setAirliner(airliner);
            aircraft.setModel(model);
            JOptionPane.showMessageDialog(null, "Add a new aircraft successfully!");
            
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed

        this.bottomPanel.remove(this);
        CardLayout cardLayout =(CardLayout)bottomPanel.getLayout();
        Component[] comps = this.bottomPanel.getComponents();
        for(Component comp : comps){
            if(comp instanceof AirlinerManagePanel){
         
                AirlinerManagePanel airlinerManagePanel = (AirlinerManagePanel) comp;
                airlinerManagePanel.populateTable(aircraftList.getAircraftDir());
            }
        }
        cardLayout.previous(bottomPanel);

    }//GEN-LAST:event_btnBackActionPerformed


