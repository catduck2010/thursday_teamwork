public void populateTable(ArrayList<Aircraft> aircraftList){
        
        DefaultTableModel dtm = (DefaultTableModel)tblAircraft.getModel();
        dtm.setRowCount(0);
        
        
        for(Aircraft aircraft : aircraftList){
            if(aircraft.getAirliner() == airliner.getProviderName()){
            Object row[] = new Object [2];
            row[0] = aircraft;
            row[1] = aircraft.getModel();
            
            dtm.addRow(row);
            }
        }
        
    }
