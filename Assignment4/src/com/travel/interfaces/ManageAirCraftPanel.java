    public void populateTable(ArrayList<Flight> flightDir){
        
        DefaultTableModel dtm = (DefaultTableModel)tblFlight.getModel();
        dtm.setRowCount(0);
        
        
        for(Flight flight : flightDir){
            if(flight.getModelNum() == modelNum){
            Object row[] = new Object [4];
            row[0] = flight;
            row[1] = flight.getDeparture();
            row[2] = flight.getArrival();
            row[3] = flight.getTakeOffTime();
            dtm.addRow(row);
            }
        }
        
    }

