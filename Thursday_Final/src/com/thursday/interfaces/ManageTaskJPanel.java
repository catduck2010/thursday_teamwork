    public void populateTable(){
        
        DefaultTableModel dtm = (DefaultTableModel)tblTask.getModel();
        dtm.setRowCount(0);
        
        
        for( Task t : WorkFlow.getAllTasks()){
            
            
            Object row[] = new Object [5];
            row[0] = t.getId();
            row[1] = t.getTitle();
            row[2] = t.getMessage();
            row[3] = t.getCreator();
            row[4] = t.getStatus();
            dtm.addRow(row);
        
        }
        
    }

